/*
* . Em JavaScript declaramos variáveis como var, let ou const
*     Detalhes:
*         . É recomendado usar let, pois foi implementado como solução de alguns problemas em var. E
*         nos casos onde deve-se garantir que aquela variável terá sempre o mesmo valor utilize const.
*         Ex: const pi = 3.14;
*/
    let n1 = 0;         // int
    let n2 = 123456789; // long
    let n3 = 1.2345;    // float
    let n4 = 1.2345678; // double
    let c1 = 'a';       // char
    
/*
* . Cada variável: n1, n2, n3, n4 e n5 foi declarada usando let, mas armazenam dados de diferentes tipos;
*/
    
/*
* . Declarando variáveis sem inicializá-las;
* . Ao fazer isto as variavéis assumem uma espécie de tipo chamado undefined
*/
let n6;
let n7;
let n8;
let n9;
let c2;
    
/*
* . É possível declarar múltiplas variáveis em apenas uma linha;
*/
